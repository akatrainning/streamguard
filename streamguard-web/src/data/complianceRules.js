export function getRuleProfile(rule) {
  const text = [rule.id, rule.title, rule.category, rule.desc, ...(rule.keywords || []), ...(rule.patterns || [])].join(" ");
  const claimTypes = CLAIM_KEYWORDS.filter(({ terms }) => terms.some((term) => text.includes(term))).map(({ claimType }) => claimType);
  const normalizedClaimTypes = claimTypes.length ? [...new Set(claimTypes)] : ["quality_claim"];
  const requiredEvidence = [...new Set([
    ...(rule.evidence || []),
    ...normalizedClaimTypes.flatMap((claimType) => CLAIM_TYPE_META[claimType]?.evidence || []),
  ])];
  const riskLevel = rule.risk === "high"
    ? (normalizedClaimTypes.includes("efficacy_claim") || normalizedClaimTypes.includes("qualification_claim") ? "P0" : "P1")
    : rule.risk === "medium"
      ? "P2"
      : "P3";
  const safeRewrite = normalizedClaimTypes.map((claimType) => CLAIM_TYPE_META[claimType]?.rewrite).find(Boolean);

  return {
    claimTypes: normalizedClaimTypes,
    requiredEvidence,
    counterEvidence: requiredEvidence.slice(0, 3).map((item) => `未检索到${item}`),
    riskLevel,
    safeRewrite,
    graph: {
      nodes: [
        { id: rule.id, type: "Rule", label: rule.title },
        ...normalizedClaimTypes.map((claimType) => ({ id: claimType, type: "ClaimType", label: CLAIM_TYPE_META[claimType]?.label || claimType })),
        ...requiredEvidence.slice(0, 3).map((item) => ({ id: item, type: "EvidenceType", label: item })),
        { id: riskLevel, type: "RiskLevel", label: RISK_LEVEL_META[riskLevel].label },
      ],
      edges: [
        ...normalizedClaimTypes.map((claimType) => ({ source: rule.id, target: claimType, label: "targets" })),
        ...requiredEvidence.slice(0, 3).map((item) => ({ source: rule.id, target: item, label: "requires" })),
        { source: rule.id, target: riskLevel, label: "leads_to" },
      ],
    },
  };
}

